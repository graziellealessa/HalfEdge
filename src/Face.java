
public class Face {
	int id, edge;

	public Face(int id, int edge) {
		super();
		this.id = id;
		this.edge = edge;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getEdge() {
		return edge;
	}

	public void setEdge(int edge) {
		this.edge = edge;
	}
	
}
